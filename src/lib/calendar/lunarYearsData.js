// lib/calendar/lunarYearsData.js

export function getLunarDay(date) {
    const year = date.getFullYear();
    const month = (date.getMonth() + 1).toString().padStart(2, '0');
    const day = date.getDate().toString().padStart(2, '0');
    const mmdd = month + day;

    // 先檢查當年的數據
    let targetYear = year;
    let monthsData = LUNAR_MONTHS_RANGE[year];

    // 如果是年初，可能需要用上一年的數據
    if (month === '01' || month === '02') {
      const prevYearData = LUNAR_MONTHS_RANGE[year - 1];
      if (prevYearData) {
        const lastMonth = prevYearData[prevYearData.length - 1];
        if (parseInt(mmdd) <= parseInt(lastMonth.end)) {
          monthsData = prevYearData;
          targetYear = year - 1;
        }
      }
    }

    if (!monthsData) return null;

    // 找到對應的農曆月份
    let foundMonth = null;
    for (const monthData of monthsData) {
      const startNum = parseInt(monthData.start);
      const endNum = parseInt(monthData.end);
      const currentNum = parseInt(mmdd);

      // 處理跨年的情況
      if (monthData.end < monthData.start) {
        if (currentNum >= startNum || currentNum <= endNum) {
          foundMonth = monthData;
          break;
        }
      } else if (currentNum >= startNum && currentNum <= endNum) {
        foundMonth = monthData;
        break;
      }
    }

    if (!foundMonth) return null;

    // 計算農曆日期
    let dayCount = 1;
    let currentMMDD = foundMonth.start;

    while (currentMMDD !== mmdd) {
      dayCount++;
      // 移到下一天
      let currentMonth = parseInt(currentMMDD.substring(0, 2));
      let currentDay = parseInt(currentMMDD.substring(2));

      // 計算月份的最後一天
      const daysInMonth = new Date(year, currentMonth, 0).getDate();

      currentDay++;
      if (currentDay > daysInMonth) {
        currentDay = 1;
        currentMonth++;
        if (currentMonth > 12) {
          currentMonth = 1;
        }
      }

      currentMMDD =
        currentMonth.toString().padStart(2, '0') +
        currentDay.toString().padStart(2, '0');

      if (dayCount > 40) break; // 防止無限循環
    }

    // 格式化輸出
    if (dayCount === 1) {
      // 初一顯示月份
      const monthDisplay = typeof foundMonth.month === 'string' ?
        `閏${foundMonth.month.replace('l', '')}` :
        foundMonth.month.toString();
      return `${monthDisplay}月`;
    } else if (dayCount <= 10) {
      // 十日以內使用 "初X"
      const chineseNumbers = ['一', '二', '三', '四', '五', '六', '七', '八', '九', '十'];
      return `初${chineseNumbers[dayCount - 1]}`;
    } else {
      // 十日以後直接顯示數字
      return dayCount.toString();
    }
  }